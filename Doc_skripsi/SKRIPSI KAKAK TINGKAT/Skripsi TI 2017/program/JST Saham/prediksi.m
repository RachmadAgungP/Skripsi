% --- Executes on button press in prediksi.
function prediksi_Callback(hObject, eventdata, handles)
% hObject    handle to prediksi (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% 

load('hasilNormalisasi.mat');
load('hasilinput.mat');
load('bobotV.mat');
load('bobotW.mat');

% memasukkan parameter========================================================================
stringX = get(handles.edit8,'String');
%
edit8 = str2num(stringX);
[bar kol]=size(edit8);
edit8(1,(kol+1))= edit8(1,1);
[bar2 kol2]=size(hasilinput);
[bar kol]=size(edit8);

for i= 1:kol
    hasilinput((bar2+1),i)=edit8(1,i);
end

% Cari Nilai Min Max
NilaiMin = min(hasilinput);
NilaiMax = max(hasilinput);

NormMinMax = NilaiMax-NilaiMin;
%
newdata = [];
for i= 1:kol
    hasi1 = (edit8(1,i)-NilaiMin(1,i))/NormMinMax(1,i);
    newdata=[newdata;hasi1];
end
newdata=newdata';

[bb kk]=size(bobotV);

[hasilTest]=protest(newdata,bb,bobotV,bobotW);
%%Denormalisasi
hasilDenorm = ceil((hasilTest*NormMinMax(1,kol))+NilaiMin(1,kol));

set(findobj(gcf,'Tag','edit11'),'String',hasilDenorm);


